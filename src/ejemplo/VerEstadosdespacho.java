    public void VerEstadosDespacho(){
        
        String[] titulos={"ID Despacho","Estado"};
        String[] registros = new String[2];
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        
        String SQL="select * from Despacho";
        
        try {
            
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            
            
            while(rs.next()){
                
                registros[0]=rs.getString("idDespacho");
                registros[1]=rs.getString("estado");
                
                modelo.addRow(registros);
                
                ListarEstado.setModel(modelo);
            
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error al cargar los datos "+ e.getMessage());
        }
    }
    
    public void BuscarEstado(String Dato){

        String[] titulos={"ID Despacho","Estado"};
        String[] registros = new String[2];
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);

        String SQL="select * from Despacho where idDespacho like '%"+Dato+"%'";

        try {

            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(SQL);


            while(rs.next()){

                registros[0]=rs.getString("idDespacho");
                registros[1]=rs.getString("estado");
                modelo.addRow(registros);

                ListarEstado.setModel(modelo);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error al cargar los datos "+ e.getMessage());
        }
    }
    
    
