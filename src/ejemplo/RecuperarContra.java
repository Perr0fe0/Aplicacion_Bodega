    public void RecuperarContra(){
        try{
            String SQL = "UPDATE Empleado set contrasenia="+txtNuevacontra+"";
            PreparedStatement pst=con.prepareStatement(SQL);
            
            pst.setString(1, txtNuevacontra.getText());
            pst.setString(2, txtVerificarcontra.getText());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Contrase単a cambiada correctamente");
            
      
            
        }catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Error al Recuperar Contrase単a "+e.getMessage());
        }
    }


